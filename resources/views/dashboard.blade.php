    var donutData        = {
      labels: productos,
      datasets: [
        {
          data: numProducto,
          backgroundColor : colores,
        }
      ]
    }
    var donutOptions     = {
      maintainAspectRatio : false,
      responsive : true,
    }
    //Create pie or douhnut chart
